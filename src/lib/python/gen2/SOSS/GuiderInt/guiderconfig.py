def nsir_exptime(mag):
    if -50.0 <= mag < 11.0:  return 600
    elif 11.0 <= mag < 12.0: return 1000
    elif 12.0 <= mag < 13.0: return 1500
    elif 13.0 <= mag < 14.0: return 2500    
    elif 14.0 <= mag < 15.0: return 4000
    elif 15.0 <= mag < 16.0: return 6000
    elif 16.0 <= mag < 17.0: return 9000
    elif 17.0 <= mag < 18.0: return 13500
    elif 18.0 <= mag < 19.0: return 22500
    else:                    return 30000    

          

